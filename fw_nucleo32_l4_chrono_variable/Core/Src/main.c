	// Each tick is 1 microseconds and time counts from 0 to 0xFFFFFFFE, so top value is 0xFFFFFFFF
	fChrono_Init(0xFFFFFFFF, 1000, (tick_t*)&(TIM2->CNT));
	
	LED_OFF_();
	
	//Start chronoTimeout object with timeout parameter 5000.
	//Unit of timeout is specified by calling fChrono_IsTimeoutS(), fChrono_IsTimeoutMs() or fChrono_IsTimeoutUs().
	fChrono_StartTimeoutMs(&chronoTimeout, 5000);
	
